					<h3>icddr,b selects Microsoft Dynamics 365 to integrate business processes and information systems. sa.global and Genex Infosys chosen as implementation partners</h3>
					<p>
					Dhaka, April 8, 2021: The International Centre for Diarrheal Disease Research, 
					Bangladesh (icddr,b) a non-profit health research institution located in Dhaka, 
					Bangladesh, has selected sa.global and Genex Infosys Limited to implement Microsoft 
					Dynamics 365 to integrate their business processes and information systems into a 
					single technology platform. Both, sa.global and Genex Infosys are Microsoft Gold 
					Partners and providers of Microsoft Dynamics 365 business applications and cloud 
					services. The companies will jointly implement the ERP system for icddr,b.
					</p>
                    <br>
                    
                    icddr,b is one of the world’s leading global health research institute committed to solving public health problems in low- and middle-income countries through innovative scientific research including laboratory-based, clinical, epidemiological, and health systems research. Established in the 1960s, the organization has been involved in high-quality research and promoting the uptake of evidence-based interventions. By developing testing and assessing the implementation of interventions specifically designed for resource-poor settings, icddr,b aims to improve the health and well-being of people living in the world’s poorest nations. 
                    <br>
                    
                    After years of growth, icddr,b was cchallenged by the limitations of their existing Microsoft NAV 2009 system which was unable to meet their increasing complex business processes. icddr,b used several disparate applications which were not connected, leading to manual input of data, data variations, and cumbersome reporting. The extensive customizations made over the years added to maintenance costs. In addition, the NAV system had reached end of support
                    <br>
                    
                    The organization was looking for a new ERP system for its approximately 5,000 users to support back-office management, research and administration, finance and operations, supply chain channels, human resources as well as their growing tactical divisions.
                    <br>
                    
                    sa.global and Genex Infosys will implement Microsoft Dynamics 365 Human Resources for performance management across the entire organization. Microsoft Dynamics 365 Finance and Operations will help icddr,b manage funding from different sources and track research projects and funds allocated to them. This will help them achieve successful outcomes for their research and to administer care through their hospital network. 
                    <br>
                    <i>
                    “Our current platform (Microsoft NAV 2009) is out of support from Microsoft; so we set out in search of a best-in-class application that would take our organization to the next level,” explained Thomas Barry,CFO icddr,b. “After evaluating several alternatives, we selected Microsoft Dynamics 365 as we feel it is designed to fully integrate our value chain so that we can gain efficiencies. We are confident that this new platform will help us grow our organization well into the future.”
                    <br>
                                    
                    “icddr,b is a well-respected non-profit addressing the most critical health concerns the world faces today,” said Kapil Gupta, Country Head – India, sa.global. “Their selection of Microsoft Dynamics 365 is indicative of their forward-thinking approach and their commitment to solve public health problems by using modern technology to manage their processes and projects. We are excited and proud to support icddr,b in their initiative and position them for future growth and success.”

                    <br>                
                        
                    “The combination of Microsoft Dynamics 365, Genex Infosys, and sa.global is a perfect fit for icddr,b” stated Abu Taiyab,ED, Genex Infosys. “The all-round capabilities in Dynamics 365 gives icddr,b users the required flexibility and expandability to support their operation for years to come. The solution also perfect alignment with their enterprise architecture with provision to adapt the modern tech like field service management, MS Guide, IOT and AI driven data analysis.Our Expert pool of consultant will help icddrb to drive the healthcare innovation and help the globe”
